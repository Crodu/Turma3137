    private void btnProcurarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcurarActionPerformed
        VendedorTableModel vtm = null;
        if (rbID.isSelected()){
            try{
                vtm = new VendedorTableModel(VendedorSingleton.getInstance().pesquisaID(Integer.parseInt(txtCampo.getText())));
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "Erro: "+e);
            }
        }else{
            vtm = new VendedorTableModel(VendedorSingleton.getInstance().pesquisaNome(txtCampo.getText(), VendedorSingleton.getInstance().vendedores));
        }
        Tabela.setModel(vtm);
    }//GEN-LAST:event_btnProcurarActionPerformed

